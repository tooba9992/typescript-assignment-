"use strict";
function describe_city(name, countary) {
    console.log(`${name} is in ${countary}`);
}
describe_city("Karachi", "Pakistan");
describe_city("Tokyo", "Japan");
describe_city("Beijing", "China");
//city names
function city_countary(name, countary) {
    console.log(`${name} , ${countary}`);
}
city_countary("Paris", "France");
city_countary("Berlin", "Germany");
city_countary("london", "United Kingdom");
